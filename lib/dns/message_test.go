func TestMessageIsExpired(t *testing.T) {
	type testCase struct {
		msg  *Message
		desc string
		exp  bool
	}

	var (
		cases []testCase
		c     testCase
		got   bool
	)

	cases = []testCase{{
		desc: "Message is not expired",
		msg: &Message{
			Answer: []ResourceRecord{{
				TTL: 1,
			}},
		},
		exp: false,
	}, {
		desc: "Message is expired",
		msg: &Message{
			Answer: []ResourceRecord{{
				TTL: 0,
			}},
		},
		exp: true,
	}}

	for _, c = range cases {
		t.Log(c.desc)

		got = c.msg.IsExpired()

		test.Assert(t, "IsExpired", c.exp, got)
	}
}

func TestMessagePackDomainName(t *testing.T) {
	type testCase struct {
		in  []byte
		exp []byte
	}

	var (
		msg = NewMessage()

		cases []testCase
		c     testCase
	)

	cases = []testCase{{
		in:  []byte("a.b"),
		exp: []byte{1, 'a', 1, 'b', 0},
	}, {
		in:  []byte("a.b."),
		exp: []byte{1, 'a', 1, 'b', 0},
	}, {
		in:  []byte("a\\.b.c"),
		exp: []byte{3, 'a', '.', 'b', 1, 'c', 0},
	}, {
		in:  []byte("a\\065.b.c"),
		exp: []byte{2, 'a', 'a', 1, 'b', 1, 'c', 0},
	}, {
		in:  []byte("a\\065"),
		exp: []byte{2, 'a', 'a', 0},
	}}

	msg.dnameOff = make(map[string]uint16)

	for _, c = range cases {
		msg.Reset()

		msg.packDomainName(c.in, false)

		test.Assert(t, string(c.in), c.exp, msg.packet)
	}
}

