func TestMessageIsExpired(t *testing.T) {
	cases := []struct {
		desc    string
		msg     *Message
		elapsed uint32
		exp     bool
	}{{
		desc: "Message is not expired",
		msg: &Message{
			Answer: []*ResourceRecord{{
				TTL: 3600,
			}},
		},
		elapsed: 3599,
		exp:     false,
	}, {
		desc: "Message is expired",
		msg: &Message{
			Answer: []*ResourceRecord{{
				TTL: 3600,
			}},
		},
		elapsed: 3600,
		exp:     true,
	}}

	for _, c := range cases {
		t.Log(c.desc)

		got := c.msg.IsExpired(c.elapsed)

		test.Assert(t, "IsExpired", c.exp, got, true)
	}
}

func TestMessagePackQuestion(t *testing.T) {
	cases := []struct {
		desc string
		msg  *Message
		exp  []byte
	}{{
		desc: "Empty name",
		msg: &Message{
			Question: &SectionQuestion{
				Type:  QueryTypeA,
				Class: QueryClassIN,
			},
			dnameOff: make(map[string]uint16),
		},
		exp: []byte{
			0x00, 0x00, 0x01, 0x00, 0x01,
		},
	}, {
		desc: "Single domain name",
		msg: &Message{
			Question: &SectionQuestion{
				Name:  []byte("kilaBit"),
				Type:  QueryTypeA,
				Class: QueryClassIN,
			},
			dnameOff: make(map[string]uint16),
		},
		exp: []byte{
			0x07, 'k', 'i', 'l', 'a', 'b', 'i', 't', 0x00,
			0x00, 0x01, 0x00, 0x01,
		},
	}, {
		desc: "Two domain names",
		msg: &Message{
			Question: &SectionQuestion{
				Name:  []byte("kilabit.info"),
				Type:  QueryTypeA,
				Class: QueryClassIN,
			},
			dnameOff: make(map[string]uint16),
		},
		exp: []byte{
			0x07, 'k', 'i', 'l', 'a', 'b', 'i', 't',
			0x04, 'i', 'n', 'f', 'o',
			0x00,
			0x00, 0x01, 0x00, 0x01,
		},
	}, {
		desc: "Three domain names",
		msg: &Message{
			Question: &SectionQuestion{
				Name:  []byte("MAIL.KILABIT.INFO"),
				Type:  QueryTypeA,
				Class: QueryClassIN,
			},
			dnameOff: make(map[string]uint16),
		},
		exp: []byte{
			0x04, 'm', 'a', 'i', 'l',
			0x07, 'k', 'i', 'l', 'a', 'b', 'i', 't',
			0x04, 'i', 'n', 'f', 'o',
			0x00,
			0x00, 0x01, 0x00, 0x01,
		},
	}}

	for _, c := range cases {
		t.Log(c.desc)

		c.msg.packQuestion()

		test.Assert(t, "packet", c.exp, c.msg.Packet, true)
	}
}

